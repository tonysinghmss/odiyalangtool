def uc_to_nuc(uc_text, nuc_to_uc_mapping):
    # invert the mapping
    uc_to_nuc_mapping = {v:k for k,v in nuc_to_uc_mapping.items()}
    # use map to convert from uni to non-uni
    nuc_text = [uc_to_nuc_mapping.get(char, char) for char in uc_text]
    return ''.join(nuc_text)
